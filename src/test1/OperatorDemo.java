public class OperatorDemo {
	public static void main(String[] args) {
		// 	Arithmatic Operator
//		+ 
//		-
//		*
//		/
//		%
		System.out.println(10+7); // 17
		System.out.println(10-7); // 3
		System.out.println(10*7); // 70
		System.out.println(10%7); // 3
		System.out.println((float)10/(float)7); // 1.4323
		// if we devide int/ int = int
		
		// 12+4/2    = 14  and 8
		// (*/%) (+-)
		// BODMAS
		System.out.println((12+4)/2);
	}
}
